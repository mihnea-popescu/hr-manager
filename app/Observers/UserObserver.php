    }

    /**
     * Handle the User "deleted" event.
     */
    public function deleted(User $user): void
    {
        $user->load([
            'processes' => [
                'fields',
                'applications' => ['fields']
            ]
        ]);

        foreach($user->processes as $process) {
            $process->fields()->delete();

            foreach($process->applications as $application) {
                $application->fields()->delete();

                $application->delete();
            }

            $process->delete();
        }
    }

